function add(a, b) {
  var number = a + b;
  return number;
}

function subtract(a, b) {
  var number = a - b;
  return number;
}

function multiply(a, b) {
  var number = a * b;
  return number;
}

function divide(a, b) {
  var number = a / b;
  return number;
}

function inc(a) {
  var increment = a++;
  return increment;
}

function dec(a) {
  var decrement = a--;
  return decrement;
}